CREATE PROCEDURE [dbo].[sp_DemoData_List]
    @WORKING_SCOPE_IDS NVARCHAR(MAX) = NULL,
    @PUBLIC_ONLY       BIT           = 0
AS
BEGIN
    SET NOCOUNT ON;
    SET QUOTED_IDENTIFIER ON;

    -- reach=AllScopes / guard bypass → tüm scope'lar (NULL). @PUBLIC_ONLY=1 ise yalniz IS_PUBLIC=1.
    IF @WORKING_SCOPE_IDS IS NULL
    BEGIN
        SELECT DEMODATA_ID, PLACE_HOLDER, STATUS, LCID, DEFAULT_LCID, OWNER_SCOPE_ID,
               CREATOR_USER_ID, CREATOR_SCOPE_ID, CREATION_TIME,
               EDITOR_USER_ID, MODIFIED_TIME
        FROM dbo.tblDemoData
        WHERE DELETED = 0
          AND (@PUBLIC_ONLY = 0 OR IS_PUBLIC = 1)
        ORDER BY DEMODATA_ID;
        RETURN;
    END

    -- scope filtresi: OWNER_SCOPE_ID IN (working set) [+ public-only]
    DECLARE @sql NVARCHAR(MAX) = N'
        SELECT DEMODATA_ID, PLACE_HOLDER, STATUS, LCID, DEFAULT_LCID, OWNER_SCOPE_ID,
               CREATOR_USER_ID, CREATOR_SCOPE_ID, CREATION_TIME,
               EDITOR_USER_ID, MODIFIED_TIME
        FROM dbo.tblDemoData
        WHERE DELETED = 0
          AND OWNER_SCOPE_ID IN (' + @WORKING_SCOPE_IDS + N')
          AND (@po = 0 OR IS_PUBLIC = 1)
        ORDER BY DEMODATA_ID';

    EXEC sp_executesql @sql, N'@po BIT', @po = @PUBLIC_ONLY;
END;
GO
